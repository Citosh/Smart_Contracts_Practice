const account = web3.eth.accounts.privateKeyToAccount(process.env.FIRST_ACCOUNT_SECRET_KEY);
web3.eth.accounts.wallet.add(account);

const tokenContract = new web3.eth.Contract(tokenABI, process.env.TOKEN_ADDRESS);

async function distributeTokens(recipients) {
    let nonce = await web3.eth.getTransactionCount(account.address, 'pending');

    for (const recipient of recipients) {
        const { address, amount } = recipient;
        const data = tokenContract.methods.mint(address, web3.utils.toWei(amount, 'ether')).encodeABI();

        const tx = {
            from: account.address,
            to: process.env.TOKEN_ADDRESS,
            gas: 3000000,
            gasPrice: web3.utils.toWei('300', 'gwei'),
            nonce: nonce++,
            data
        };

        try {
            const receipt = await web3.eth.sendTransaction(tx);
            console.log(`Успішно нараховано ${amount} токенів на адресу ${address}`);
            console.log(receipt);
        } catch (error) {
            console.error(`Помилка для адреси ${address}: ${error.message}`);
        }
    }
}

